//sjekker om man kan se en blokk
function sight(map, pPos, mPos){
    var a = (mPos[1]-pPos[1])/(mPos[0]-pPos[0])
    for(x=pPos[0]; x<mPos[0]; x+=0.01){
        if(Math.floor(pPos[1]+(x-pPos[0])*a)==py && Math.floor(x)==px){
            return true
        }
        if(map[Math.floor(pPos[1]+(x-pPos[0])*a)][Math.floor(x)]!=9){
            return false
        }
    }
    for(x=pPos[0]; x>mPos[0]; x-=0.01){
        if(Math.floor(pPos[1]+(x-pPos[0])*a)==py && Math.floor(x)==px){
            return true
        }
        if(map[Math.floor(pPos[1]+(x-pPos[0])*a)][Math.floor(x)]!=9){
            return false
        }
    }
    return true
}

const collisionPrec = 10
const g = 0.00004
function update(){
    //movement og collision
    if(player.moving){
        if(player.direction=="left"){
            player.vx = -0.005
            player.img = movingLeftImgs[Math.floor(frameCount%60/15)]
        }
        else{
            player.vx = 0.005
            player.img = movingRightImgs[Math.floor(frameCount%60/15)]
        }
    }
    else{
        frameCount = 0
        if(player.falling){
            player.vx-=player.vx/200
        }
        else{
            player.vx = 0
        }
    }
    if(player.vx>0){
        if(map[Math.floor(player.y+20/64)][Math.floor(player.x+1-7/32)]!=9 || map[Math.floor(player.y+1+20/64)][Math.floor(player.x+1-7/32)]!=9 || map[Math.floor(player.y+1.99)][Math.floor(player.x+1-7/32)]!=9){
            player.vx = 0
        }
    }
    if(player.vx<0){
        if(map[Math.floor(player.y+20/64)][Math.floor(player.x+7/32)]!=9 || map[Math.floor(player.y+1+20/64)][Math.floor(player.x+7/32)]!=9 || map[Math.floor(player.y+1.99)][Math.floor(player.x+7/32)]!=9){
            player.vx = 0
        }
    }
    if(player.vy<0){
        if(map[Math.floor(player.y+20/64)][Math.floor(player.x+7/32)]!=9 || map[Math.floor(player.y+20/64)][Math.floor(player.x+1-7/32)]!=9){
            player.vy = 0
        }
    }
    player.x+=player.vx
    if(map[Math.round(player.y+2)][Math.floor(player.x+7/32)]==9 && map[Math.round(player.y+2)][Math.floor(player.x+1-7/32)]==9){
        player.falling = true
    }
    if(player.falling){
        if(map[Math.floor(player.y+2)][Math.round(player.x)]!=9 && player.vy>0){
            player.falling = false
            //fikser at man setter seg fast i bakken
            player.y = Math.round(player.y)
            player.vy = 0
        }
        else{
            player.y+=player.vy
            player.vy+=g
        }
    }

}
